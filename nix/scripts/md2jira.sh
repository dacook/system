## simple convert Markdown to Jira
# Requires: OSX (-E)
sed -E \
-e "s/\*\*/\*/g" \
-e "s/^(  +)(\*) /\1*\2 /g" \
-e "s/^(  +)(\-) /\1*\2 /g" \
-e "s/^(  +)(\+) /\1*\2 /g" \
-e "s/^#### /h4. /g" \
-e "s/^### /h3. /g" \
-e "s/^## /h2. /g" \
-e "s/^# /h1. /g" \
-e "s/^[0-9]+\. /# /g" \
-e "s/~~(.*)~~/ -\1- /g" \
-e "s/\[(.*)\]\(([^\)]*)\)/[\1|\2]/g" \
-e "s/\`([^\`]*)\`/{{\1}}/g" \
$1 > $2
